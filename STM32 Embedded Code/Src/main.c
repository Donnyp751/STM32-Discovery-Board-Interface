//For reading a single register at a time from the HTS211
uint8_t readHTSReg(uint8_t address){
	rxBuffer[0] = address;//place the desired address in the first position as the sub addr pointer

	status = HAL_I2C_Master_Transmit(&hi2c2, HTSADDRWR, rxBuffer,1,100);//send the sub addr to be received

	status = HAL_I2C_Master_Receive(&hi2c2, HTSADDRRD, rxBuffer, 1,100);//receive a byte from the sub addr sent before

	return rxBuffer[0];
}
