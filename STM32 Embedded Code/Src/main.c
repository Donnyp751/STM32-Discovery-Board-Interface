void initHTS221(){

	  rxBuffer[0] = 0x10; //AV_CONF register address pointer
	  rxBuffer[1] = 0b00011011; //16 temp samples averaged, 32 humidity samples averaged

	  status = HAL_I2C_Master_Transmit(&hi2c2, HTSADDRWR, rxBuffer,2,500); //transmit the AV_CONF to the temp sensor
	  HAL_Delay(20);

	  rxBuffer[0] = 0x20; //CTRL_REG1
	  rxBuffer[1] = 0b10000010; //Active mode, and 7hz update rate for humidity and temp

	  status = HAL_I2C_Master_Transmit(&hi2c2, HTSADDRWR, rxBuffer,2,500); //Transmit the CTRL_REG to the sensor
	  HAL_Delay(20);

	  //Calibration coefficients retrieval below
	  H0 = readHTSReg(0x30) / 2; //stored multiplied by two
	  H1 = readHTSReg(0x31) / 2; //stored multiplied by two

	  H0_T0 = readHTSReg(0x36) | (readHTSReg(0x37)<<8); //grab the lsb and msb and convert to signed 16 bit int
	  H1_T0 = readHTSReg(0x3A) | (readHTSReg(0x3B)<<8); //grab the lsb and msb and convert to signed 16 bit int

	  /*The temperature calibration coefficients are 10 bit values where
	   *the MSB's of T0, and T1 is in address 0x35, and LSB's
	   *are in 0x32, and 0x33 */
	  uint8_t T1T0MSBs = readHTSReg(0x35); //b0 = T0.8, b1 = T0.9, b2 = T1.8, b3 = T1.9
	  uint8_t T0LSB = readHTSReg(0x32); //T0 lsb
	  uint8_t T1LSB = readHTSReg(0x33); //T1 lsb

	  T0 = ((T1T0MSBs & 0x03)<<8) | T0LSB; //converting into 10 bit value
	  T1 = ((T1T0MSBs & 0x0C)<<6) | T1LSB; //converting into 10 bit value

	  T0_OUT = (int16_t)(readHTSReg(0x3C) | (readHTSReg(0x3D)<<8)); //grab the lsb and msb and convert to signed 16 bit int
	  T1_OUT = (int16_t)(readHTSReg(0x3E) | (readHTSReg(0x3F)<<8)); //grab the lsb and msb and convert to signed 16 bit int
}

float getTemp(){
	int rawTemp = readHTSReg(0x2A) | (readHTSReg(0x2B)<<8); //read in the 16 bit raw temp value

	if(rawTemp > INT16_MAX)
	{	//for bounding into 16 bit range
		rawTemp -= UINT16_MAX;
	}

	/*Calculate the actual temperature using the raw ADC reading, the calibration coefficients, and some linear interpolation */
	float cTemp = (((T1 - T0) / 8.0) * (rawTemp - T0_OUT) / (T1_OUT - T0_OUT) + (T0/8.0));

	return cTemp;
}

float getHumid(){
	int16_t rawHumid = readHTSReg(0x28) | (readHTSReg(0x29)<<8); //read in the 16 bit raw humid value

	/*Calculate the actual humidity using the raw ADC reading, the calibration coefficients, and some linear interpolation */
	float humidity = ((1.0 * H1) - (1.0 * H0)) * ((1.0 * rawHumid) - (1.0 * H0_T0)) / ((1.0 * H1_T0) - (1.0 * H0_T0)) + (1.0 * H0);
	return humidity;
}
